/*=============== APPLY TRANSLATIONS ===============*/
function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  document.documentElement.lang = lang;
}

/*=============== LANGUAGE TOGGLE ===============*/
const savedLang = localStorage.getItem('selected-lang') ||
  (navigator.language.startsWith('pt') ? 'pt-br' : 'en');

let currentLang = savedLang;

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('selected-lang', lang);
  applyTranslations(lang);

  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active-lang', opt.dataset.lang === lang);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  setLang(currentLang);

  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.addEventListener('click', () => setLang(opt.dataset.lang));
  });
});
